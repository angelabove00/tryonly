    private void refreshInfo()
    {
        jTextArea1.setText("");
        if (emp == null || att == null) {
            jTextArea1.setText("Please Update Missing Data!");
                }
        else if (emp != null && att != null) {
            jTextArea1.setText("Employee Name: " + emp.getName() + "\nEmployee ID: " + emp.getEmployeeNumber() + "\nBirthday: " + emp.getBirthday() + "\nContact Number: " + emp.getContactInfo() + "\nJob Position: " + emp.getPosition() + "\nDepartment: " + emp.getDepartment() + "\n\n\nHours Worked: " + att.calculateTotalHours());
        }
        else {
            jTextArea1.setText("Please Update Missing Data!");
        }
    }

